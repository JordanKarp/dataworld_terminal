def remove_last_vowel(word, min_length=4):
    if len(word) <= min_length:
        return word
    vowels = "aeiouAEIOU"
    word_list = list(word)

    # Iterate over the word in reverse to find the last vowel
    for i in range(len(word_list) - 1, -1, -1):
        if word_list[i] in vowels:
            del word_list[i]
            break

    return "".join(word_list)


def remove_last_letter(word, min_length=4):
    return word[:-1] if len(word) > min_length else word


def capitalize_first(word):
    return word[0].upper() + word[1:] if word else word


def lower_first(word):
    return word[0].lower() + word[1:] if word else word


